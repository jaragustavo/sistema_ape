<br>
                            Es muy grato y un alto honor hoy estar frente a ustedes, apreciados colegas de todo el país
                            para presentar el Informe y las memorias de estos primeros años de trabajo al frente de
                            nuestra prestigiosa Asociación Paraguaya de Enfermería que fue posible gracias al trabajo de
                            la Junta Directiva, presidentes de Filiales, asesoras y gran equipo administrativo.
                            </p>

                            <p>Estimados compañeros de todo el país, son grandes los pasos que la profesión dio en los
