                                Enfermeras y Enfermeros, el pago por el turno nocturno, los concursos para el ingreso al
                                ejercido de la profesión, también el concurso para cargos gerenciales y la seguridad en
                                el
                                trabajo.
                                <br>

                                Cuidar la salud mental es prioridad, todo lo podemos lograr con la unidad es el factor
                                fundamental para el avance.
                                <br>

                                Si se piensa en seguir caminando es indiscutible que no debemos retroceder en la perdida
                                de
                                ningún beneficio conquistado y esa es la lucha más dura que vamos a afrontar.
