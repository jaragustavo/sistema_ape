                                        el Estatuto Social y el reconocimiento de la Personería Jurídica.

                                        El 25 de julio de 1958 a través del decreto 35.094 se declara el libre Ejercicio
                                        de la Profesión de Enfermería en todo el territorio de la República.

                                        Entre los objetivos de esta agremiación puede destacarse:
                                    <ul>
                                        <li>Fomentar el mejoramiento de la salud de la población del país a través del
                                            desenvolvimiento profesional, educacional y cultural de las enfermeras.</li>
                                        <li>Promover una atención profesional adecuada al individuo sano y enfermo
                                            mediante el mejoramiento constante de los servicios de enfermería, estudiar
                                            las
                                            situaciones relacionadas con el ejercicio de la profesión.</li>
                                        <li>Establecer becas para el mejor ejercicio de la profesión.</li>
                                        <li>Relacionar la Asociación con grupo afines, nacionales e internacionales para
